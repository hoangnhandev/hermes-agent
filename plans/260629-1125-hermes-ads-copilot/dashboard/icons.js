/**
 * Render an inline SVG icon.
 * @param {string} name  key in ICONS
 * @param {object} opts  { size, class, strokeWidth }
 * @returns {string} svg markup (uses currentColor)
 */
function icon(name, opts) {
  const o = opts || {};
  const size = o.size || 20;
  const cls = o.class || '';
  const sw = o.strokeWidth || 2;
  const inner = ICONS[name] || '';
  return (
    '<svg xmlns="http://www.w3.org/2000/svg" width="' + size + '" height="' + size +
    '" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="' + sw +
    '" stroke-linecap="round" stroke-linejoin="round" class="' + cls +
    '" aria-hidden="true" focusable="false">' + inner + '</svg>'
  );
}

/**
 * Replace every `[data-icon="name"]` element's innerHTML with the matching
 * SVG. Called once on boot and again after dynamic tab content is injected
 * (so empty states / KPI icons inside fetched fragments also render).
 */
function hydrateIcons(root) {
  const scope = root || document;
  scope.querySelectorAll('[data-icon]').forEach((el) => {
    el.innerHTML = icon(el.getAttribute('data-icon'));
  });
}

if (typeof window !== 'undefined') {
  window.icon = icon;
  window.ICONS = ICONS;
  window.hydrateIcons = hydrateIcons;
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => hydrateIcons());
  } else {
    hydrateIcons();
  }
}
